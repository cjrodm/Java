public class EjemploClaseMath {
    public static void main(String[] args) throws Exception {
       
        System.out.println();
        System.out.println("Math.abs(-3) = " + Math.abs(-3));
        System.out.println("Math.max(3.1, 1.2) = " + Math.max(3.1, 1.2));
        System.out.println("Math.min(3.5, 1.2) = " + Math.min(3.5, 1.2));
        System.out.println("Math.ceil(3.5) = " + Math.ceil(3.5));
        System.out.println("Math.floor(3.5) = " + Math.floor(3.5));
        System.out.println("Math.round(3.5) = " + Math.round(3.5));
        System.out.println("Math.PI = "+Math.PI);
        System.out.println("Math.exp(1) = " + Math.exp(1));
        System.out.println("Math.pow(10, 3) = "+Math.pow(10, 3));
        System.out.println("Math.sqrt(5) = "+Math.sqrt(5));
        System.out.println("Math.round(Math.toDegrees(1.57)) = "+Math.round(Math.toDegrees(1.57)));
        System.out.println("Math.toRadians(90.00) = "+Math.toRadians(90.00));
        System.out.println("Math.sin(Math.toRadians(90.00)) = "+Math.sin(Math.toRadians(90.00)));
        System.out.println("Math.cos(Math.toRadians(90.00)) = "+Math.cos(Math.toRadians(90.00)));

    }
}
